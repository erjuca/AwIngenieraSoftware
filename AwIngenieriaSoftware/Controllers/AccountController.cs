        //
        // POST: /Account/Register
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterViewModel2 model)
        {
            string rolId = Convert.ToString(Request.Form["RolLista"]);
            if (string.IsNullOrEmpty(rolId))
            {
                ModelState.AddModelError("", "El campo Rol es requerido.");
            }
            else
            {
                model.Rol = rolId;
            }
            if (ModelState.IsValid)
            {
                if (string.IsNullOrEmpty(model.Email))
                {
                    model.Email = "correo@correo.com";
                }

                var userCtrl = UserManager.Users.Where(x => x.UserName.Equals(model.UserName)).Any();
                if (userCtrl)
                {
                    ModelState.AddModelError("", "El usuario ya existe, ingrese uno diferente");
                }
                else
                {
                    var user = new ApplicationUser { UserName = model.UserName, Email = model.Email, ClienteId = 0, ClienteNombre = model.FullName2 };
                    var cliente = new Cliente { Identificacion = model.Identificacion, Nombres = model.Nombres, Apellidos = model.Apellidos, Direccion = model.Direccion, Telefono = model.Telefono, Estado = "ACTIVO", Genero = model.Genero, Mail = model.Email };
                    int idCliente = 0;
                    if (!db.Cliente.Any(x => x.Identificacion.Equals(cliente.Identificacion.Trim())))
                    {
                        db.Cliente.Add(cliente);
                        idCliente = cliente.Id;
                    }
                    else
                    {
                        cliente = db.Cliente.Where(x => x.Identificacion.Equals(cliente.Identificacion.Trim())).First();
                        db.Entry(cliente).State = EntityState.Modified;
                        idCliente = cliente.Id;
                    }
                    db.SaveChanges();
                    user.ClienteId = idCliente;
                    user.ClienteNombre = cliente.FullName;
                    var result = await UserManager.CreateAsync(user, model.Password);

                    if (result.Succeeded)
                    {
                        var roles = UserManager.GetRoles(user.Id);
                        if (!roles.Contains(model.Rol))
                            result = UserManager.AddToRole(user.Id, model.Rol);
                        if (result.Succeeded == true)
                        {
                            await SignInManager.SignInAsync(user, isPersistent: false, rememberBrowser: false);

                            // Para obtener más información sobre cómo habilitar la confirmación de cuentas y el restablecimiento de contraseña, visite https://go.microsoft.com/fwlink/?LinkID=320771
                            // Enviar correo electrónico con este vínculo
                            //string code = await UserManager.GenerateEmailConfirmationTokenAsync(user.Id);
                            //var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = user.Id, code = code }, protocol: Request.Url.Scheme);
                            //await UserManager.SendEmailAsync(user.Id, "Confirmar cuenta", "Para confirmar la cuenta, haga clic <a href=\"" + callbackUrl + "\">aquí</a>");
                            return RedirectToAction("Index", "Home");
                        }
                    }
                    AddErrors(result);
                }
            }

            // Si llegamos a este punto, es que se ha producido un error y volvemos a mostrar el formulario
            ViewBag.Generos = new SelectList(db.Catalogo.Where(x => x.Codigo == 4 && !x.Argumento.Trim().Equals("")), "Valor", "Descripcion");
            ViewBag.RolLista = GetRoles(rolId);
            return View(model);
        }

        private List<SelectListItem> GetRoles(string idRol)
        {
            List<SelectListItem> Roles = new List<SelectListItem>();
            Roles.Add(
                new SelectListItem
                {
                    Text = "Seleccione uno",
                    Value = ""
                });

            var result = RoleManager.Roles.ToList();
            foreach (var item in result)
            {
                if (item.Name.Equals("VOLUNTARIO") || item.Name.Equals("FUNDACION"))
                {
                    SelectListItem itemList= new SelectListItem
                    {
                        Text = item.Name.ToString(),
                        Value = item.Name.ToString(),
                        Selected = false
                    };
                    if (idRol.Equals(item.Name))
                    {
                        itemList.Selected = true;
                    }
                    Roles.Add(itemList);
                }

            }
            return Roles;
        }

